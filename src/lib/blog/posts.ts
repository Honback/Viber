/** Published posts only — used by public pages */
export function getAllPosts(): BlogPost[] {
  return [...posts]
    .filter((post) => post.status === "published")
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
}

/** All posts including drafts — used by admin/review */
export function getAllPostsWithDrafts(): BlogPost[] {
  return [...posts].sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
}

export function getPostBySlug(slug: string): BlogPost | undefined {
  return posts.find((post) => post.slug === slug && post.status === "published");
}

export function getDraftBySlug(slug: string): BlogPost | undefined {
  return posts.find((post) => post.slug === slug);
}

export function getAllSlugs(): string[] {
  return posts.filter((post) => post.status === "published").map((post) => post.slug);
}

export function getDraftCount(): number {
  return posts.filter((post) => post.status === "draft").length;
}
